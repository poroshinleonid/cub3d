int	ft_atoi_cub(char *s)
{
	int	res;

	if (!s || *s < '0' || *s > '9')
		return (-1);
	res = 0;
	while (*s >= '0' && *s <= '9')
	{
		res *= 10;
		res += *(s++) - '0';
		if (res > 255)
			return (-1);
	}
	if (*s != '\0')
		return (-1);
	return (res);
}